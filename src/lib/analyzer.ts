const ALL_DETECTORS: Detector[] = [
  ...watermarkDetectors,
  ...stackDetectors,
  ...copyDetectors,
  ...structureDetectors,
  ...visualDetectors,
  ...placeholderDetectors,
];

/** Strip tags/scripts/styles to approximate visible text. */
function extractText(html: string): string {
  return html
    .replace(/<script[\s\S]*?<\/script>/gi, " ")
    .replace(/<style[\s\S]*?<\/style>/gi, " ")
    .replace(/<[^>]+>/g, " ")
    .replace(/&nbsp;/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function pickVerdict(score: number): Verdict {
  if (score <= 18)
    return { label: "Suspiciously Human", emoji: "🧑‍🎨", blurb: "Bespoke and opinionated. A real person sweated over this." };
  if (score <= 38)
    return { label: "Mostly Handcrafted", emoji: "🛠️", blurb: "Custom work with a few convenient shortcuts. Tasteful." };
  if (score <= 58)
    return { label: "AI-Assisted", emoji: "🤝", blurb: "A human and a model clearly co-wrote this one." };
  if (score <= 78)
    return { label: "Heavily AI-Generated", emoji: "🤖", blurb: "The agent did most of the driving. Few human fingerprints." };
  return { label: "Textbook AI Slop", emoji: "🪐", blurb: "Default skill, default stack, default everything. Peak vibe-code." };
}

export function analyze(
  html: string,
  headers: Record<string, string>,
  url: string,
  finalUrl: string
): AnalysisResult {
  const lower = html.toLowerCase();
  const text = extractText(html);
  const ctx: Ctx = { html, lower, text, headers, url };

  const signals: Signal[] = [];
  for (const d of ALL_DETECTORS) {
    try {
      const s = d(ctx);
      if (s && s.points > 0) signals.push(s);
    } catch {
      // a single detector blowing up should never sink the analysis
    }
  }

  // Aggregate per-category, capping each category at its max so no single
  // dimension can run away with the score.
  const categories: CategoryScore[] = (Object.keys(CATEGORY_META) as SignalCategory[]).map((cat) => {
    const catSignals = signals.filter((s) => s.category === cat);
    const raw = catSignals.reduce((sum, s) => sum + s.points, 0);
    const meta = CATEGORY_META[cat];
    return {
      category: cat,
      title: meta.title,
      emoji: meta.emoji,
      points: Math.min(raw, meta.max),
      max: meta.max,
      signals: catSignals.sort((a, b) => b.points - a.points),
    };
  });

  const score = Math.max(
    0,
    Math.min(100, Math.round(categories.reduce((sum, c) => sum + c.points, 0)))
  );

  const titleMatch = html.match(/<title[^>]*>([^<]*)<\/title>/i);
  const genMatch = html.match(/<meta[^>]+name=["']generator["'][^>]+content=["']([^"']+)["']/i);

  return {
    url,
    finalUrl,
    score,
    verdict: pickVerdict(score),
    categories: categories.sort((a, b) => b.points - a.points),
    topSignals: [...signals].sort((a, b) => b.points - a.points).slice(0, 5),
    meta: {
      title: titleMatch ? titleMatch[1].trim().slice(0, 120) : null,
      generator: genMatch ? genMatch[1] : null,
      bytes: html.length,
      fetchedAt: new Date().toISOString(),
    },
  };
}
